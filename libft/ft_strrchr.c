#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t			len;
	unsigned char	new_c;

	len = ft_strlen(s);
	new_c = (unsigned char)c;
	while (s[len] != new_c && len > 0)
		len--;
	if (s[len] == c)
		return ((char *)(s + len));
	return (0);
}
