#include "libft.h"

void	ft_putstr(char *s)
{
	size_t	len;
	size_t	i;

	if (!s)
		return ;
	len = ft_strlen(s);
	i = 0;
	while (i < len)
		write(1, &s[i++], 1);
	write(1, "\n", 1);
}
