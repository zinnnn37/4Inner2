#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	if (n < 10)
	{
		c = n + '0';
		write(1, &c, 1);
		return ;
	}
	ft_putnbr(n / 10);
	c = n % 10 + '0';
	write(1, &c, 1);
}
